export function LanguageProvider({ children }: { children: ReactNode }) {
  // Force reset to German on first load
  React.useEffect(() => {
    if (typeof window !== 'undefined') {
      localStorage.removeItem('app-language')
    }
  }, [])
  
  const [language, setLanguage] = useKV<Language>('app-language', 'de')

  // Force German as default language
  React.useEffect(() => {
    if (language !== 'de') {
      setLanguage('de')
    }
  }, [])

  const t = (key: string): string => {
    try {
      const currentLang = language || 'de'
      // Ensure the language is valid
      const validLang = ['en', 'de', 'zh', 'fr'].includes(currentLang) ? currentLang : 'de'
      return translations[validLang as Language]?.[key] || translations.de[key] || key
    } catch (error) {
      console.warn('Translation error for key:', key, error)
      return key
    }
  }

  const safeSetLanguage = (lang: Language) => {
    try {
      // Validate language before setting
      if (['en', 'de', 'zh', 'fr'].includes(lang)) {
        setLanguage(lang)
      } else {
        console.warn('Invalid language:', lang, 'defaulting to German')
        setLanguage('de')
      }
    } catch (error) {
      console.error('Error setting language:', error)
      setLanguage('de')
    }
  }

  const contextValue = {
    language: language || 'de',
    setLanguage: safeSetLanguage,
    t
  }

  return (
    <LanguageContext.Provider value={contextValue}>
      {children}
    </LanguageContext.Provider>
  )
}

export function useLanguage() {
  const context = useContext(LanguageContext)
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider')
  }
  return context
}